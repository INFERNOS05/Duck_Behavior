// SwimBehavior.java

// This is the SwimBehavior interface, which defines the contract for any swimming behavior.
// Any class implementing this interface must provide a specific implementation for the swim() method.
public interface SwimBehavior {
    
    // The swim() method is intended to define the swimming behavior of the class that implements this interface.
    // The method must be implemented to perform the swimming action.
    void swim();
}

