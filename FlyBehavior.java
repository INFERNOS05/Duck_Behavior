// FlyBehavior.java

// This is the FlyBehavior interface, which defines the contract for any flying behavior.
// Any class implementing this interface must provide a specific implementation for the fly() method.
public interface FlyBehavior {
    
    // The fly() method is intended to define the flying behavior of the class that implements this interface.
    // The method must be implemented to perform the flying action.
    void fly();
}

