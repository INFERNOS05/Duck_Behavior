// QuackBehavior.java

// This is the QuackBehavior interface, which defines the contract for any quacking behavior.
// Any class implementing this interface must provide a specific implementation for the quack() method.
public interface QuackBehavior {
    
    // The quack() method is intended to define the quacking behavior of the class that implements this interface.
    // The method must be implemented to perform the quacking action.
    void quack();
}

